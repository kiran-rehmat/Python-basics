# OPERATORS IN PYTHON

# Arithmetic Operators
a = 10
b = 3
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)  # removes decimal part
print("Modulus:", a % b)
print("Power:", a ** b)  # a raised to b

# Comparison Operators (always return True/False)
print("Is a > b?", a > b)
print("Is a == b?", a == b)
print("Is a != b?", a != b)