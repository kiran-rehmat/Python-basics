# VARIABLES AND DATA TYPES

# A variable stores data that can be used later in the program
name = "Kiran"       # string
age = 20             # integer
height = 5.4         # float
is_student = True    # boolean

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is student:", is_student)

# Type checking
print("Type of name:", type(name))
print("Type of age:", type(age))